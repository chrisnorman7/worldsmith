/// Provides various operators used by conditional classes.
import 'stat_condition.dart';

/// The operator used by the [StatCondition] class.
enum ConditionalOperator {
  /// ==
  equal,

  /// !=
  notEqual,

  /// <
  lessThan,

  /// <=
  lessEqual,

  /// >
  greaterThan,

  /// >=
  greaterEqual
}
